-- =====================================================================
-- Elimina cursos sin driver: CAPAC_CURSOS donde IDRIVER IS NULL o no
-- referencia a un IDRIVER existente en CAPAC_DRIVERS.
--
-- Borra primero las filas dependientes por ICURSO en:
--   - CAPAC_ATRIBUTOS_PLANES
--   - CAPAC_PLANES_CURSOS
--   - CAPAC_SEGURIDADES_CURSOS
--   - CAPAC_ESTRUCTURAS_CURSOS
--   - CAPAC_CURSOS_DE_PLANES_ESTUDIO
--   - CAPAC_CURSOS_PREREQUISITOS (por ICURSO o ICURSOREQUERIDO)
--   - CAPAC_HISTORIALCURSO
-- y luego CAPAC_CURSOS. Toda la operación va en una transacción.
-- Idempotente: si no hay cursos huérfanos, no afecta filas.
-- =====================================================================

SET NOCOUNT ON;
SET XACT_ABORT ON;

IF OBJECT_ID('CAPAC_CURSOS', 'U') IS NULL
BEGIN
    PRINT 'CAPAC_CURSOS no existe. Operación omitida.';
    RETURN;
END

BEGIN TRAN;

-- Conjunto de cursos a eliminar
DECLARE @orphans TABLE (ICURSO VARCHAR(255) PRIMARY KEY);

INSERT INTO @orphans (ICURSO)
SELECT c.ICURSO
  FROM CAPAC_CURSOS c
 WHERE c.IDRIVER IS NULL
    OR (
        OBJECT_ID('CAPAC_DRIVERS', 'U') IS NOT NULL
        AND NOT EXISTS (
            SELECT 1 FROM CAPAC_DRIVERS d WHERE d.IDRIVER = c.IDRIVER
        )
       );

DECLARE @total INT = (SELECT COUNT(*) FROM @orphans);
PRINT 'Cursos sin driver detectados: ' + CAST(@total AS VARCHAR(20));

IF @total = 0
BEGIN
    COMMIT;
    RETURN;
END

-- Dependencias por ICURSO
IF OBJECT_ID('CAPAC_ATRIBUTOS_PLANES', 'U') IS NOT NULL
BEGIN
    DELETE FROM CAPAC_ATRIBUTOS_PLANES
     WHERE ICURSO IN (SELECT ICURSO FROM @orphans);
    PRINT 'CAPAC_ATRIBUTOS_PLANES · filas eliminadas: ' + CAST(@@ROWCOUNT AS VARCHAR(20));
END

IF OBJECT_ID('CAPAC_PLANES_CURSOS', 'U') IS NOT NULL
BEGIN
    DELETE FROM CAPAC_PLANES_CURSOS
     WHERE ICURSO IN (SELECT ICURSO FROM @orphans);
    PRINT 'CAPAC_PLANES_CURSOS · filas eliminadas: ' + CAST(@@ROWCOUNT AS VARCHAR(20));
END

IF OBJECT_ID('CAPAC_SEGURIDADES_CURSOS', 'U') IS NOT NULL
BEGIN
    DELETE FROM CAPAC_SEGURIDADES_CURSOS
     WHERE ICURSO IN (SELECT ICURSO FROM @orphans);
    PRINT 'CAPAC_SEGURIDADES_CURSOS · filas eliminadas: ' + CAST(@@ROWCOUNT AS VARCHAR(20));
END

IF OBJECT_ID('CAPAC_ESTRUCTURAS_CURSOS', 'U') IS NOT NULL
BEGIN
    DELETE FROM CAPAC_ESTRUCTURAS_CURSOS
     WHERE ICURSO IN (SELECT ICURSO FROM @orphans);
    PRINT 'CAPAC_ESTRUCTURAS_CURSOS · filas eliminadas: ' + CAST(@@ROWCOUNT AS VARCHAR(20));
END

IF OBJECT_ID('CAPAC_CURSOS_DE_PLANES_ESTUDIO', 'U') IS NOT NULL
BEGIN
    DELETE FROM CAPAC_CURSOS_DE_PLANES_ESTUDIO
     WHERE ICURSO IN (SELECT ICURSO FROM @orphans);
    PRINT 'CAPAC_CURSOS_DE_PLANES_ESTUDIO · filas eliminadas: ' + CAST(@@ROWCOUNT AS VARCHAR(20));
END

IF OBJECT_ID('CAPAC_CURSOS_PREREQUISITOS', 'U') IS NOT NULL
BEGIN
    DELETE FROM CAPAC_CURSOS_PREREQUISITOS
     WHERE ICURSO IN (SELECT ICURSO FROM @orphans)
        OR ICURSOREQUERIDO IN (SELECT ICURSO FROM @orphans);
    PRINT 'CAPAC_CURSOS_PREREQUISITOS · filas eliminadas: ' + CAST(@@ROWCOUNT AS VARCHAR(20));
END

IF OBJECT_ID('CAPAC_HISTORIALCURSO', 'U') IS NOT NULL
BEGIN
    DELETE FROM CAPAC_HISTORIALCURSO
     WHERE ICURSO IN (SELECT ICURSO FROM @orphans);
    PRINT 'CAPAC_HISTORIALCURSO · filas eliminadas: ' + CAST(@@ROWCOUNT AS VARCHAR(20));
END

DELETE FROM CAPAC_CURSOS
 WHERE ICURSO IN (SELECT ICURSO FROM @orphans);
PRINT 'CAPAC_CURSOS · cursos eliminados: ' + CAST(@@ROWCOUNT AS VARCHAR(20));

COMMIT;
PRINT 'Migración completada: cursos sin driver eliminados.';
