DROP FUNCTION IF EXISTS "BD_ISADOC"."_absorb_table"(TEXT, TEXT);
DROP FUNCTION IF EXISTS "BD_ISADOC"."_absorb_sequence"(TEXT, TEXT);

DROP SCHEMA IF EXISTS "BD_PATY" CASCADE;
DROP SCHEMA IF EXISTS "BD_CLIENTESIS" CASCADE;

