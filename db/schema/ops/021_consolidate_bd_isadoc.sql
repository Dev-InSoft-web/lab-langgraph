DO $$
BEGIN
	IF EXISTS (SELECT 1 FROM pg_tables WHERE schemaname='BD_ISADOC' AND tablename='STORE_STOREPROJECT')
	   AND NOT EXISTS (SELECT 1 FROM pg_tables WHERE schemaname='BD_ISADOC' AND tablename='STORE_PROJECT') THEN
		ALTER TABLE "BD_ISADOC"."STORE_STOREPROJECT" RENAME TO "STORE_PROJECT";
	END IF;
	IF EXISTS (SELECT 1 FROM pg_tables WHERE schemaname='BD_ISADOC' AND tablename='STORE_STORESECTION')
	   AND NOT EXISTS (SELECT 1 FROM pg_tables WHERE schemaname='BD_ISADOC' AND tablename='STORE_SECTION') THEN
		ALTER TABLE "BD_ISADOC"."STORE_STORESECTION" RENAME TO "STORE_SECTION";
	END IF;
	IF EXISTS (SELECT 1 FROM pg_tables WHERE schemaname='BD_ISADOC' AND tablename='ENTITY_ENTITYDEFINITION')
	   AND NOT EXISTS (SELECT 1 FROM pg_tables WHERE schemaname='BD_ISADOC' AND tablename='ENTITY_DEFINITION') THEN
		ALTER TABLE "BD_ISADOC"."ENTITY_ENTITYDEFINITION" RENAME TO "ENTITY_DEFINITION";
	END IF;
	IF EXISTS (SELECT 1 FROM pg_tables WHERE schemaname='BD_ISADOC' AND tablename='BITACORA_BITACORAREVISADO')
	   AND NOT EXISTS (SELECT 1 FROM pg_tables WHERE schemaname='BD_ISADOC' AND tablename='BITACORA_REVISADO') THEN
		ALTER TABLE "BD_ISADOC"."BITACORA_BITACORAREVISADO" RENAME TO "BITACORA_REVISADO";
	END IF;
	IF EXISTS (SELECT 1 FROM pg_tables WHERE schemaname='BD_ISADOC' AND tablename='APICATALOG_APICATALOGMANIFEST')
	   AND NOT EXISTS (SELECT 1 FROM pg_tables WHERE schemaname='BD_ISADOC' AND tablename='APICATALOG_MANIFEST') THEN
		ALTER TABLE "BD_ISADOC"."APICATALOG_APICATALOGMANIFEST" RENAME TO "APICATALOG_MANIFEST";
	END IF;
	IF EXISTS (SELECT 1 FROM pg_tables WHERE schemaname='BD_ISADOC' AND tablename='ENTITY_ENTITYROW')
	   AND NOT EXISTS (SELECT 1 FROM pg_tables WHERE schemaname='BD_ISADOC' AND tablename='ENTITY_ROW') THEN
		ALTER TABLE "BD_ISADOC"."ENTITY_ENTITYROW" RENAME TO "ENTITY_ROW";
	END IF;
END;
$$;

