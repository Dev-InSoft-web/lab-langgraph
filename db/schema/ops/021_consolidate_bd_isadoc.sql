-- Unifica BD_PATY + BD_CLIENTESIS → BD_ISADOC (único almacén PostgreSQL de ISA-DOC).

DO $$ BEGIN
	IF EXISTS (SELECT 1 FROM pg_namespace WHERE nspname = 'BD_ISADOC')
	   AND NOT EXISTS (SELECT 1 FROM pg_namespace WHERE nspname = 'BD_PATY')
	   AND NOT EXISTS (SELECT 1 FROM pg_namespace WHERE nspname = 'BD_CLIENTESIS') THEN
		RETURN;
	END IF;
END $$;

CREATE SCHEMA IF NOT EXISTS "BD_ISADOC";

CREATE OR REPLACE FUNCTION "BD_ISADOC"."_absorb_table"(
	p_from_schema TEXT, p_table TEXT
) RETURNS VOID LANGUAGE plpgsql AS $$
BEGIN
	IF NOT EXISTS (
		SELECT 1 FROM information_schema.tables
		WHERE table_schema = p_from_schema AND table_name = p_table
	) THEN
		RETURN;
	END IF;
	IF p_from_schema = 'BD_ISADOC' THEN
		RETURN;
	END IF;
	IF NOT EXISTS (
		SELECT 1 FROM information_schema.tables
		WHERE table_schema = 'BD_ISADOC' AND table_name = p_table
	) THEN
		BEGIN
			EXECUTE format('ALTER TABLE %I.%I SET SCHEMA "BD_ISADOC"', p_from_schema, p_table);
		EXCEPTION WHEN duplicate_object THEN
			NULL;
		END;
		RETURN;
	END IF;
	IF p_table = 'ENTITY_ROW' THEN
		EXECUTE format(
			'INSERT INTO "BD_ISADOC"."ENTITY_ROW" SELECT * FROM %I.%I ON CONFLICT DO NOTHING',
			p_from_schema, p_table
		);
	ELSE
		EXECUTE format(
			'INSERT INTO "BD_ISADOC".%I SELECT * FROM %I.%I ON CONFLICT DO NOTHING',
			p_table, p_from_schema, p_table
		);
	END IF;
	EXECUTE format('DROP TABLE %I.%I', p_from_schema, p_table);
END;
$$;

CREATE OR REPLACE FUNCTION "BD_ISADOC"."_absorb_sequence"(
	p_from_schema TEXT, p_seq TEXT
) RETURNS VOID LANGUAGE plpgsql AS $$
BEGIN
	IF NOT EXISTS (SELECT 1 FROM pg_sequences WHERE schemaname = p_from_schema AND sequencename = p_seq) THEN
		RETURN;
	END IF;
	IF p_from_schema = 'BD_ISADOC' THEN RETURN; END IF;
	IF NOT EXISTS (SELECT 1 FROM pg_sequences WHERE schemaname = 'BD_ISADOC' AND sequencename = p_seq) THEN
		EXECUTE format('ALTER SEQUENCE %I.%I SET SCHEMA "BD_ISADOC"', p_from_schema, p_seq);
	END IF;
END;
$$;

DO $$
DECLARE
	src TEXT;
	tables TEXT[] := ARRAY[
		'ENTITY_ROW', 'STORE_PROJECT', 'STORE_SECTION', 'ENTITY_DEFINITION',
		'BITACORA_REVISADO', 'APICATALOG_MANIFEST',
		'STORE_STOREPROJECT', 'STORE_STORESECTION', 'ENTITY_ENTITYDEFINITION',
		'BITACORA_BITACORAREVISADO', 'APICATALOG_APICATALOGMANIFEST', 'ENTITY_ENTITYROW'
	];
	seqs TEXT[] := ARRAY[
		'SEQ_STORE_PROJECT', 'SEQ_STORE_SECTION', 'SEQ_ENTITY_DEFINITION',
		'SEQ_STORE_STOREPROJECT', 'SEQ_STORE_STORESECTION', 'SEQ_ENTITY_ENTITYDEFINITION'
	];
	langlab_orphans TEXT[] := ARRAY[
		'STORE_STOREPROJECT', 'STORE_STORESECTION', 'ENTITY_ENTITYDEFINITION',
		'BITACORA_BITACORAREVISADO', 'APICATALOG_APICATALOGMANIFEST', 'ENTITY_ENTITYROW',
		'ENTITY_ROW', 'STORE_PROJECT', 'STORE_SECTION', 'ENTITY_DEFINITION',
		'BITACORA_REVISADO', 'APICATALOG_MANIFEST'
	];
	t TEXT;
	s TEXT;
BEGIN
	-- Siempre absorber BD_PATY / BD_CLIENTESIS aunque BD_ISADOC ya exista.
	FOREACH src IN ARRAY ARRAY['BD_PATY', 'BD_CLIENTESIS', 'BD_LANGLAB'] LOOP
		IF NOT EXISTS (SELECT 1 FROM pg_namespace WHERE nspname = src) THEN CONTINUE; END IF;
		FOREACH t IN ARRAY tables LOOP
			PERFORM "BD_ISADOC"."_absorb_table"(src, t);
		END LOOP;
		FOREACH s IN ARRAY seqs LOOP
			PERFORM "BD_ISADOC"."_absorb_sequence"(src, s);
		END LOOP;
	END LOOP;

	FOREACH t IN ARRAY langlab_orphans LOOP
		IF EXISTS (SELECT 1 FROM pg_tables WHERE schemaname = 'BD_LANGLAB' AND tablename = t) THEN
			EXECUTE format('DROP TABLE "BD_LANGLAB".%I', t);
		END IF;
	END LOOP;
END;
$$;

