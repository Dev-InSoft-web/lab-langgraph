CREATE SEQUENCE IF NOT EXISTS "BD_ISADOC"."SEQ_STORE_SECTION" AS bigint START 1;
CREATE SEQUENCE IF NOT EXISTS "BD_ISADOC"."SEQ_ENTITY_DEFINITION" AS bigint START 1;

CREATE TABLE IF NOT EXISTS "BD_ISADOC"."ENTITY_ROW" (
	"PROJECT" TEXT NOT NULL,
	"PAGE" TEXT NOT NULL,
	"ENTITY" TEXT NOT NULL,
	"IENTITYID" TEXT NOT NULL,
	"BODY" JSONB NOT NULL DEFAULT '{}',
