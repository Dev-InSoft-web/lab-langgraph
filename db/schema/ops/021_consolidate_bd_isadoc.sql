CREATE INDEX IF NOT EXISTS "IX_ENTITY_ROW_SEGMENT" ON "BD_ISADOC"."ENTITY_ROW" ("PROJECT", "PAGE", "ENTITY");
CREATE INDEX IF NOT EXISTS "IX_ENTITY_ROW_PARENT" ON "BD_ISADOC"."ENTITY_ROW" ("PARENTPROJECT", "PARENTPAGE", "PARENTENTITY", "IPARENTENTITYID") WHERE "IPARENTENTITYID" IS NOT NULL;
